class appliance_redirect
{
/**
* name of action buttons
* @access public
* @var string
*/
var $actions_name = 'appliance_action';
/**
* message param
* @access public
* @var string
*/
var $message_param = "appliance_msg";
/**
* id for tabs
* @access public
* @var string
*/
var $prefix_tab = 'appliance_tab';
/**
* identifier name
* @access public
* @var string
*/
var $identifier_name = 'appliance_identifier';
/**
* path to templates
* @access public
* @var string
*/
var $tpldir;
/**
* translation
* @access public
* @var array
*/
var $lang = array();

	//--------------------------------------------
	/**
	 * Constructor
	 *
	 * @access public
	 * @param htvcenter $htvcenter
	 * @param htmlobject_response $response
	 */
	//--------------------------------------------
	function __construct($htvcenter, $response) {
		$this->response   = $response;
		$this->file       = $htvcenter->file();
		$this->htvcenter    = $htvcenter;
	}

	//--------------------------------------------
	/**
	 * Action
	 *
	 * @access public
	 * @return htmlobject_template
	 */
	//--------------------------------------------
	function action() {
		$plugin = $this->response->html->request()->get('target');
		$class  = $this->htvcenter->get('basedir').'/plugins/'.$plugin.'/class/'.$plugin.'.controller.class.php';
		#echo $class;
		if($this->file->exists($class)) {
			$url = $this->response->html->thisfile.'?plugin='.$plugin;
		} else {
			$url = $this->response->html->thisfile.'?iframe='.$this->htvcenter->get('baseurl').'/plugins/'.$plugin.'/'.$plugin.'-manager.php';
		}
		if(isset($url)) {
			$this->response->redirect($url);
		}
	}

}
