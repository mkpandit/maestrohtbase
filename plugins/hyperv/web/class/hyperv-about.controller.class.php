class hyperv_about_controller
{
/**
* name of action buttons
* @access public
* @var string
*/
var $actions_name = 'hyperv_about_action';
/**
* message param
* @access public
* @var string
*/
var $message_param = 'hyperv_about_msg';
/**
* id for tabs
* @access public
* @var string
*/
var $prefix_tab = 'hyperv_about_tab';
/**
* identifier name
* @access public
* @var string
*/
var $identifier_name = 'hyperv_about_identifier';
/**
* path to templates
* @access public
* @var string
*/
var $tpldir;
/**
* translation
* @access public
* @var array
*/
var $lang = array(
	'documentation' => array (
		'tab' => 'About Hyper-V',
		'label' => 'About Hyper-V',
		'introduction_title' => 'Introduction',
		'introduction_content' => 'The "Hyper-V" plugin integrates Hyper-V-Server.',

		'requirements_title' => 'Requirements',
		'requirements_list' => 'Hyper-V Server integrated in htvcenter',
		'tested_title' => 'Tested with',
		'tested_content' => 'This plugin is tested with the Hyper-V on Windows Server 2012',
		'provides_title' => 'Provides',
		'provides_list' => 'Virtualization types: "Hyper-V Host" and "Hyper-V VM"',
		'type_title' => 'Plugin Type',
		'type_content' => 'Virtualization',
		'documentation_title' => 'Documentation',
		'use_case_title' => 'Use-Case',
	),
	
	'usage' => array (
		'tab' => 'About Hyper-V',
		'label' => 'Hyper-V Use-Cases',
	),
);

	//--------------------------------------------
	/**
	 * Constructor
	 *
	 * @access public
	 * @param htvcenter $htvcenter
	 * @param htmlobject_response $response
	 */
	//--------------------------------------------
	function __construct($htvcenter, $response) {
		$this->htvcenter  = $htvcenter;
		$this->user     = $this->htvcenter->user();
		$this->rootdir  = $this->htvcenter->get('webdir');
		$this->response = $response;
		$this->file     = $this->htvcenter->file();
		$this->lang     = $this->user->translate($this->lang, $this->rootdir."/plugins/hyperv/lang", 'hyperv-about.ini');
		$this->tpldir   = $this->rootdir.'/plugins/hyperv/tpl';
	}

	//--------------------------------------------
	/**
	 * Action
	 *
	 * @access public
	 * @param string $action
	 * @return htmlobject_tabmenu
	 */
	//--------------------------------------------
	function action($action = null) {
		$this->action = '';
		$ar = $this->response->html->request()->get($this->actions_name);
		if($ar !== '') {
			$this->action = $ar;
		}
		else if(isset($action)) {
			$this->action = $action;
		}
		if($this->response->cancel()) {
			$this->action = "documentation";
		}
		$content = array();
		switch( $this->action ) {
			case '':
			case 'documentation':
				$content[] = $this->documentation(true);
			break;
			case 'usage':
				$content[] = $this->usage(true);
			break;
		}
		$tab = $this->response->html->tabmenu($this->prefix_tab);
		$tab->message_param = $this->message_param;
		$tab->css = 'htmlobject_tabs';
		$tab->add($content);
		return $tab;
	}


	//--------------------------------------------
	/**
	 * About Hyper-V
	 *
	 * @access public
	 * @param bool $hidden
	 * @return array
	 */
	//--------------------------------------------
	function documentation( $hidden = true ) {
		$data = '';
		if( $hidden === true ) {
			require_once($this->rootdir.'/plugins/hyperv/class/hyperv-about.documentation.class.php');
			$controller = new hyperv_about_documentation($this->htvcenter, $this->response);
			$controller->actions_name    = $this->actions_name;
			$controller->tpldir          = $this->tpldir;
			$controller->message_param   = $this->message_param;
			$controller->lang            = $this->lang['documentation'];
			$data = $controller->action();
		}
		$content['label']   = $this->lang['documentation']['tab'];
		$content['value']   = $data;
		$content['target']  = $this->response->html->thisfile;
		$content['request'] = $this->response->get_array($this->actions_name, 'documentation' );
		$content['onclick'] = false;
		if($this->action === 'documentation'){
			$content['active']  = true;
		}
		return $content;
	}


	//--------------------------------------------
	/**
	 * About Hyper-V Use-Cases
	 *
	 * @access public
	 * @param bool $hidden
	 * @return array
	 */
	//--------------------------------------------
	function usage( $hidden = true ) {
		$data = '';
		if( $hidden === true ) {
			require_once($this->rootdir.'/plugins/hyperv/class/hyperv-about.usage.class.php');
			$controller = new hyperv_about_usage($this->htvcenter, $this->response);
			$controller->actions_name    = $this->actions_name;
			$controller->tpldir          = $this->tpldir;
			$controller->message_param   = $this->message_param;
			$controller->lang            = $this->lang['usage'];
			$data = $controller->action();
		}
		$content['label']   = $this->lang['usage']['tab'];
		$content['value']   = $data;
		$content['target']  = $this->response->html->thisfile;
		$content['request'] = $this->response->get_array($this->actions_name, 'usage' );
		$content['onclick'] = false;
		if($this->action === 'usage'){
			$content['active']  = true;
		}
		return $content;
	}




}
